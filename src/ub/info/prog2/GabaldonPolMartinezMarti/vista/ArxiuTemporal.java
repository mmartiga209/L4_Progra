public class ArxiuTemporal {
    private String cami;
    private String autor;
    private String codec;
    private int pixelsAmplada;
    private int pixelsAlsada;
    private int kbs;
    private String camiImatge;
    
    public void setCami(String cami) {
        this.cami = cami;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public void setCodec(String codec) {
        this.codec = codec;
    }

    public void setPixelsAmplada(int pixelsAmplada) {
        this.pixelsAmplada = pixelsAmplada;
    }

    public void setPixelsAlsada(int pixelsAlsada) {
        this.pixelsAlsada = pixelsAlsada;
    }

    public void setKbs(int kbs) {
        this.kbs = kbs;
    }

    public void setCamiImatge(String camiImatge) {
        this.camiImatge = camiImatge;
    }

    public String getCami() {
        return cami;
    }

    public String getAutor() {
        return autor;
    }

    public String getCodec() {
        return codec;
    }

    public int getPixelsAmplada() {
        return pixelsAmplada;
    }

    public int getPixelsAlsada() {
        return pixelsAlsada;
    }

    public int getKbs() {
        return kbs;
    }

    public String getCamiImatge() {
        return camiImatge;
    }



    
    
}
